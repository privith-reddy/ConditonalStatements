public class PositiveNegativeNumber {

	public static void main(String[] args) 
	{
		int num = 10;
		if (num>0) 
		{
			System.out.println("positive number"); 	
		}
		else if (num<0) 
		{
			System.out.println("negative number");	
		}
		else 
		{
			System.out.println("number is zero");
			
		}

	}

}
